let currentChapter = null;
let currentPage = 0;

// Load chapters in dropdown menu
function loadChapters() {
    const chapterList = document.getElementById("chapterList");
    for (const [chapter, pages] of Object.entries(chapters)) {
        const a = document.createElement("a");
        a.className = "dropdown-item text-light";
        a.textContent = `Chapter ${chapter}`;
        a.href = "#";
        a.onclick = () => selectChapter(parseInt(chapter));
        chapterList.appendChild(a);
    }
}

// Select a chapter
function selectChapter(chapter) {
    currentChapter = chapter;
    currentPage = 0;
    document.getElementById("chapterTitle").textContent = `Chapter ${chapter}`;
    updatePageDisplay();
}

// Update page display with the current image
function updatePageDisplay() {
    const pageDisplay = document.getElementById("pageDisplay");
    const pages = chapters[currentChapter];
    
    if (pages && pages.length > 0) {
        pageDisplay.innerHTML = `<img src="${pages[currentPage]}" alt="Page Image">`;
        document.getElementById("prevButton").disabled = currentPage === 0;
        document.getElementById("nextButton").disabled = currentPage === pages.length - 1;
    }
}

// Show previous page
function showPreviousPage() {
    if (currentPage > 0) {
        currentPage--;
        updatePageDisplay();
    }
}

// Show next page
function showNextPage() {
    const pages = chapters[currentChapter];
    if (pages && currentPage < pages.length - 1) {
        currentPage++;
        updatePageDisplay();
    }
}

// Toggle light and dark mode
function toggleMode() {
    document.body.classList.toggle("dark-mode");
    document.body.classList.toggle("light-mode");
}

// Initialize the app
loadChapters();
